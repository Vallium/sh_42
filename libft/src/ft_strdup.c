#include <stdlib.h>
#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*dst;

	dst = (char*)malloc(ft_strlen(s1) * sizeof(char) + sizeof(char));
	if (!dst)
		return (NULL);
	return (ft_strcpy(dst, s1));
}
